function displayRecipes(filteredRecipes = recipes) {
    const book = document.getElementById('book');
    book.innerHTML = ''; // Clear existing content

    if (filteredRecipes.length === 0) {
        book.innerHTML = '<p>No recipes found.</p>';
        return;
    }

    filteredRecipes.forEach((recipe, index) => {
        const recipePage = document.createElement('div');
        recipePage.className = 'book-page';
        recipePage.innerHTML = `
            <img src="${recipe.image}" alt="${recipe.name}" style="width: 600px; height: 350px; object-fit: cover;">
            <h3>${recipe.name}</h3>
            <p><strong>Category:</strong> ${capitalizeFirstLetter(recipe.category)}</p>
            <p><strong>Ingredients:</strong> ${recipe.ingredients}</p>
            <p><strong>Instructions:</strong> ${recipe.instructions}</p>
        `;

        // Add click event to each recipe for deleting
        recipePage.addEventListener('click', function () {
            // Clear existing buttons if any
            const existingButtons = recipePage.querySelectorAll('button');
            existingButtons.forEach(button => button.remove());

            // Create delete button dynamically when clicking the recipe
            const deleteButton = document.createElement('button');
            deleteButton.textContent = 'Delete';
            deleteButton.addEventListener('click', function (e) {
                e.stopPropagation(); // Prevent triggering parent click
                deleteRecipe(index);
            });

            // Append delete button to the recipe page
            recipePage.appendChild(deleteButton);
        });

        book.appendChild(recipePage);
    });
}

// Capitalize the first letter of a string and replace hyphens with spaces
function capitalizeFirstLetter(string) {
    return string.charAt(0).toUpperCase() + string.slice(1).replace('-', ' ');
}

// Add a new recipe
function addRecipe(event) {
    event.preventDefault();
    const name = document.getElementById('recipeName').value;
    const image = document.getElementById('recipeImage').value;
    const ingredients = document.getElementById('recipeIngredients').value;
    const instructions = document.getElementById('recipeInstructions').value;
    const category = document.getElementById('recipeCategory').value;

    const newRecipe = {
        name,
        image,
        ingredients,
        instructions,
        category
    };
    recipes.push(newRecipe);
    localStorage.setItem('recipes', JSON.stringify(recipes));
    displayRecipes(); // Display updated recipes list immediately
}

// Search recipes by name or ingredients
function searchRecipes() {
    const searchTerm = document.getElementById('searchBar').value.toLowerCase();
    const filteredRecipes = recipes.filter(recipe =>
        recipe.name.toLowerCase().includes(searchTerm) ||
        recipe.ingredients.toLowerCase().includes(searchTerm)
    );
    displayRecipes(filteredRecipes);
}

// Filter recipes by category
function filterByCategory() {
    const selectedCategory = document.getElementById('categoryFilter').value;
    if (selectedCategory === "all") {
        displayRecipes(recipes);
    } else {
        const filteredRecipes = recipes.filter(recipe => recipe.category === selectedCategory);
        displayRecipes(filteredRecipes);
    }
}

// Delete a recipe
function deleteRecipe(index) {
    recipes.splice(index, 1); // Remove the recipe from the array
    localStorage.setItem('recipes', JSON.stringify(recipes)); // Update localStorage
    displayRecipes(); // Re-render the recipes
}

// On page load, display recipes
window.onload = function() {
    displayRecipes(); // Display recipes on page load
    // Optionally, load recipes from localStorage if available
    const savedRecipes = localStorage.getItem('recipes');
    if (savedRecipes) {
        recipes = JSON.parse(savedRecipes);
        displayRecipes();
    }
};

// Event listeners for search and filter
document.getElementById('searchBar').addEventListener('input', searchRecipes);
document.getElementById('categoryFilter').addEventListener('change', filterByCategory);
